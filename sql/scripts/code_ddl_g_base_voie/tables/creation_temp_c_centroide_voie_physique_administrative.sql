COMMENT ON TABLE G_BASE_VOIE.TEMP_C_CENTROIDE_VOIE_PHYSIQUE_ADMINISTRATIVE IS 'Table - du projet C de correction de la latéralité des voies - contenant les centroïdes des voies physiques.';
