COMMENT ON COLUMN G_BASE_VOIE.TEMP_C_CENTROIDE_VOIE_PHYSIQUE_ADMINISTRATIVE.id_voie_administrative IS 'Identifiant de la voie administrative.';
COMMENT ON COLUMN G_BASE_VOIE.TEMP_C_CENTROIDE_VOIE_PHYSIQUE_ADMINISTRATIVE.id_voie_physique IS 'Identifiant de la voie physique.';
COMMENT ON COLUMN G_BASE_VOIE.TEMP_C_CENTROIDE_VOIE_PHYSIQUE_ADMINISTRATIVE.id_troncon IS 'Identifiant du tronçon.';
COMMENT ON COLUMN G_BASE_VOIE.TEMP_C_CENTROIDE_VOIE_PHYSIQUE_ADMINISTRATIVE.code_insee IS 'Code INSEE de chaque voie administrative.';

